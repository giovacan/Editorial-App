export const getCommunityBooks = () => COMMUNITY_BOOKS;

export const getCommunityBooksByCategory = (category) => {
  if (!category || category === 'all') return COMMUNITY_BOOKS;
  return COMMUNITY_BOOKS.filter(book => book.category === category);
};

export const getFeaturedBooks = () => {
  return COMMUNITY_BOOKS.filter(book => book.isFeatured);
};

export const getBestsellers = () => {
  return [...COMMUNITY_BOOKS].sort((a, b) => b.downloads - a.downloads).slice(0, 8);
};

export const getTopRated = () => {
  return [...COMMUNITY_BOOKS].sort((a, b) => b.rating - a.rating).slice(0, 8);
};

export const getNewBooks = () => {
  return [...COMMUNITY_BOOKS].sort((a, b) => new Date(b.publishedDate) - new Date(a.publishedDate)).slice(0, 8);
};

export const getBooksByAuthor = (authorName) => {
  if (!authorName) return [];
  return COMMUNITY_BOOKS.filter(book => book.author === authorName);
};

export const getBookById = (id) => {
  return COMMUNITY_BOOKS.find(book => book.id === id);
};

export default COMMUNITY_BOOKS;
